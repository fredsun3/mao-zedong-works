export function getPeriodById(id: string): Period | undefined {
  return periods.find((p) => p.id === id);
}

export function getPoemById(periodId: string, poemId: string): Poem | undefined {
  const period = getPeriodById(periodId);
  return period?.poems.find((p) => p.id === poemId);
}

export function getAllPoems(): { poem: Poem; period: Period }[] {
  return periods.flatMap((period) =>
    period.poems.map((poem) => ({ poem, period }))
  );
}

export function getAllQuotes(): { quote: Quote; period: Period }[] {
  return periods.flatMap((period) =>
    period.quotes.map((quote) => ({ quote, period }))
  );
}

export function getAllArticles(): { article: Article; period: Period }[] {
  return periods.flatMap((period) =>
    period.articles.map((article) => ({ article, period }))
  );
}
